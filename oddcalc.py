
num1 = int(input("Enter first number "))
num2 = int(input("Enter second number "))
operator = input("What operator will you choose? ")

if operator == "mul":print("The result of ",num1,"*",num2,"=",num1*num2)
if operator == "div":print("The result of ",num1,"/",num2,"=",num1/num2)
if operator == "mod":print("The result of ",num1,"%",num2,"=",num1%num2)
